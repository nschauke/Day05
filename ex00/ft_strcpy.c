#include <unistd.h>

char 		*ft_strcpy(char *dest, char *src)

{

	int r;

	r = 0;

	while(src[r]!= '\0')

	{
		dest[r] = src[r];
		r++;

	}

	dest[r] = '\0';

	return (dest);

}
